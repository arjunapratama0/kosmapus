      Sekilas Tentang KosMaPus
     </h2>
     <p class="about-content">
      KosMaPus adalah perusahaan proptech yang meningkatkan kualitas hunian sewa di Indonesia melalui penyediaan lifestyle residence dengan layanan lengkap dan hospitality yang unggul. Rukita kini tersedia di 21 kota besar yang tersebar di Pulau Jawa, Sumatra, dan Bali dan telah melayani lebih dari 30.000 penghuni.
     </p>
     <p class="about-content">
      Pada Juli 2022, Rukita mendapatkan penghargaan dengan predikat Best Proptech Start Up - Indonesia di ajang Global Brands Magazine Awards 2022. Penghargaan berskala internasional ini menjadi pengakuan atas komitmen Rukita terhadap inovasi, kualitas, aktivitas branding, layanan pelanggan dan kinerja bisnis, serta pengembangan sistem proptech yang kuat di Indonesia. Pada tahun yang sama, Rukita juga mendapatkan predikat "Best of The Best" di dua ajang sekaligus, yaitu Indonesia Customer Service Champions (ICSC) dan Indonesia Digital Marketing Champions (IDMC) yang diselenggarakan oleh SWA Magazine dan Business Digest.
     </p>
